fun main(args: Array<String>) {
    var floatLiteral: Float = 178.95F //literal float
    println("contoh literal float : " + floatLiteral)
    var oneBillion: Long = 100000000L //Literal Long
    println("contoh literasi long: " + oneBillion)
    val valHeksa = 0x0F //LITERAL HEKSADESIMAL DIAWALI DENGAN 0X
    println("contoh literasi Heksadesimal: " + valHeksa)
    val valBinary = 0b0001011 //literal binary diawali dengan 0b
    println("contoh literasi Binary: " + valBinary)
    val notasiKonvensional = 123.5e10
    println("contoh literasi konvensional floating point Number :" + notasiKonvensional)
}